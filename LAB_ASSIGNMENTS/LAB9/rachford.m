function x=rachford(k,z)
    c1=0;c2=0;
    for i=1:1:4
        c1=c1+(z(i)/k(i));
        c2=c2+(z(i)*k(i));
    end
    f=@(x)((z(1)*(k(1)-1))/(1+x*(k(1)-1)))+((z(2)*(k(2)-1))/(1+x*(k(2)-1)))+((z(3)*(k(3)-1))/(1+x*(k(3)-1)))+((z(4)*(k(4)-1))/(1+x*(k(4)-1)));
    %if(c1>0 && c2>0)
        x=fsolve(f,1);
    %end
end